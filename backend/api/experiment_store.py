"""
Off-repo experiment code storage.

Stores experiment code in ~/.mad-experiments/ (configurable via MAD_STORE_DIR).
Each experiment gets a directory with its code tarball + extracted files.
Code is hashed for integrity verification.

Layout:
  ~/.mad-experiments/
    042/
      code/           — extracted code files
      code.tar.gz     — compressed archive
      manifest.json   — file list, sizes, hash
"""

import hashlib
import json
import os
import shutil
import tarfile
import tempfile
from datetime import datetime
from io import BytesIO
from pathlib import Path
from typing import Optional

STORE_DIR = Path(os.environ.get("MAD_STORE_DIR", Path.home() / ".mad-experiments"))


def _ensure_store() -> None:
    STORE_DIR.mkdir(parents=True, exist_ok=True)


def _exp_dir(experiment_id: str) -> Path:
    return STORE_DIR / experiment_id


def store_code(experiment_id: str, source_dir: Path) -> dict:
    """
    Copy experiment code from source_dir into the store.

    Args:
        experiment_id: e.g. "042"
        source_dir: path to the code directory (e.g. code/042/)

    Returns:
        dict with code_dir, code_hash, manifest
    """
    _ensure_store()
    dest = _exp_dir(experiment_id)

    # Clean previous if exists
    if dest.exists():
        shutil.rmtree(dest)

    code_dest = dest / "code"
    code_dest.mkdir(parents=True)

    # Copy all files
    manifest_files = []
    for src_file in sorted(source_dir.rglob("*")):
        if src_file.is_file():
            rel = src_file.relative_to(source_dir)
            # Skip common non-essential files
            if any(part.startswith(".") for part in rel.parts):
                continue
            if rel.parts[0] == "__pycache__":
                continue

            dst_file = code_dest / rel
            dst_file.parent.mkdir(parents=True, exist_ok=True)
            shutil.copy2(src_file, dst_file)
            manifest_files.append({
                "path": str(rel),
                "size": src_file.stat().st_size,
            })

    # Create tarball
    tar_path = dest / "code.tar.gz"
    with tarfile.open(tar_path, "w:gz") as tar:
        tar.add(code_dest, arcname="code")

    # Hash the tarball
    h = hashlib.sha256()
    with open(tar_path, "rb") as f:
        for chunk in iter(lambda: f.read(8192), b""):
            h.update(chunk)
    code_hash = h.hexdigest()

    # Write manifest
    manifest = {
        "experiment_id": experiment_id,
        "files": manifest_files,
        "total_files": len(manifest_files),
        "code_hash": code_hash,
        "stored_at": datetime.utcnow().isoformat() + "Z",
    }
    (dest / "manifest.json").write_text(json.dumps(manifest, indent=2))

    return {
        "code_dir": str(code_dest),
        "code_hash": code_hash,
        "manifest": manifest,
    }


def store_code_from_dict(experiment_id: str, files: dict[str, str]) -> dict:
    """
    Store code from a dict of {relative_path: content}.

    Useful when code is generated by an agent and not yet on disk.

    Args:
        experiment_id: e.g. "042"
        files: {"train.py": "import torch...", "config.yaml": "..."}

    Returns:
        dict with code_dir, code_hash, manifest
    """
    _ensure_store()

    # Write to a temp directory, then use store_code
    with tempfile.TemporaryDirectory() as tmpdir:
        tmp = Path(tmpdir)
        for rel_path, content in files.items():
            fp = tmp / rel_path
            fp.parent.mkdir(parents=True, exist_ok=True)
            fp.write_text(content)
        return store_code(experiment_id, tmp)


def get_code_dir(experiment_id: str) -> Optional[Path]:
    """Get path to stored code directory, or None if not stored."""
    code_dir = _exp_dir(experiment_id) / "code"
    return code_dir if code_dir.exists() else None


def get_manifest(experiment_id: str) -> Optional[dict]:
    """Get the manifest for a stored experiment."""
    manifest_path = _exp_dir(experiment_id) / "manifest.json"
    if not manifest_path.exists():
        return None
    return json.loads(manifest_path.read_text())


def get_file(experiment_id: str, relative_path: str) -> Optional[str]:
    """Read a single file from the store."""
    code_dir = get_code_dir(experiment_id)
    if code_dir is None:
        return None
    fp = code_dir / relative_path
    if not fp.exists() or not fp.is_file():
        return None
    try:
        return fp.read_text()
    except UnicodeDecodeError:
        return f"<binary file: {fp.stat().st_size} bytes>"


def list_files(experiment_id: str) -> list[str]:
    """List all files in a stored experiment."""
    manifest = get_manifest(experiment_id)
    if manifest:
        return [f["path"] for f in manifest["files"]]
    # Fallback to scanning
    code_dir = get_code_dir(experiment_id)
    if code_dir is None:
        return []
    return [str(f.relative_to(code_dir)) for f in sorted(code_dir.rglob("*")) if f.is_file()]


def verify_integrity(experiment_id: str) -> bool:
    """Verify stored code matches its hash."""
    manifest = get_manifest(experiment_id)
    if manifest is None:
        return False

    tar_path = _exp_dir(experiment_id) / "code.tar.gz"
    if not tar_path.exists():
        return False

    h = hashlib.sha256()
    with open(tar_path, "rb") as f:
        for chunk in iter(lambda: f.read(8192), b""):
            h.update(chunk)

    return h.hexdigest() == manifest["code_hash"]


def delete_experiment(experiment_id: str) -> bool:
    """Remove stored code for an experiment."""
    dest = _exp_dir(experiment_id)
    if dest.exists():
        shutil.rmtree(dest)
        return True
    return False
